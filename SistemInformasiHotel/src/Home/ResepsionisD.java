    public void tmplkmr() throws SQLException {
        DefaultTableModel tmplkmr = new DefaultTableModel();
        tmplkmr.addColumn("No. Kamar");
        tmplkmr.addColumn("Tingkat");
        tmplkmr.addColumn("Kondisi");
        tmplkmr.addColumn("Tanggal Masuk");
        tmplkmr.addColumn("Harga");
        Kmr.setModel(tmplkmr);
        try {
            rs = stmt.executeQuery("SELECT * FROM kamar");
            while (rs.next()) {
                tmplkmr.addRow(new Object[]{
                    rs.getString("NoKamar"),
                    rs.getString("Tingkat"),
                    rs.getString("Kondisi"),
                    rs.getString("DateIn"),
                    rs.getString("Harga"),});
            }

        } catch (SQLException e) {
            System.out.println(e);
        }
    }

