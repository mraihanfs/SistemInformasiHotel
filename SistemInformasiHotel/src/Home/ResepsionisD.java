    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        char JK;
        String noL;
        noL = dF.format(dNow) + noKmrF.getText();
        if ("Laki - Laki".equals((jkC.getItemAt(jkC.getSelectedIndex())))) {
            JK = 'L';
        } else {
            JK = 'P';
        }
        try {
            koneksi();
            stmt.executeUpdate("INSERT INTO visitor VALUES('" + idNF.getText() + "', '" + nameF.getText() + "', '" + addressF.getText() + "', '" + JK + "')");
            stmt.executeUpdate("UPDATE kamar SET Kondisi='Digunakan', DateIn='" + dF.format(dNow) + "' WHERE NoKamar='" + noKmrF.getText() + "'");
            stmt.executeUpdate("INSERT INTO laporan (NoLaporan, NoKamar, NIK, NamaV, Harga, DateIn) VALUE('" + noL + "', '" + noKmrF.getText() + "', '" + idNF.getText() + "', '" + nameF.getText() + "', '" + pKmrF.getText() + "', '" + dF.format(dNow) + "')");
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            formV.setVisible(false);
            tmplkmr();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_SaveActionPerformed

