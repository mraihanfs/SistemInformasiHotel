    private void checkOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkOutActionPerformed
        TableModel model = Kmr.getModel();
        String stats = (String) model.getValueAt(i, 2);
        String dIn = (String) model.getValueAt(i, 3);
        Date dateIn = null;
        try {
            dateIn = new SimpleDateFormat("yyyy-MM-dd").parse(dIn);
        } catch (ParseException ex) {
            System.out.println(ex);
        }
        long dDiff = dNow.getTime() - dateIn.getTime();
        int rDiff = (int) (dDiff / (1000 * 60 * 60 * 24));
        int harga = Integer.parseInt(model.getValueAt(i, 4).toString());
        long total;
        total = rDiff * (long) harga;
        String NoL;
        NoL = (String) model.getValueAt(i, 3) + (String) model.getValueAt(i, 0);
        if ("Digunakan".equals(stats)) {
            int input = JOptionPane.showConfirmDialog(null, "Apakah Tamu sudah membayar ? ", "Konfirmasi Pembayaran",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if (input == 0) {
                try {
                    koneksi();
                    stmt.executeUpdate("UPDATE kamar SET Kondisi = 'Available', DateIn = NULL WHERE NoKamar = '" + model.getValueAt(i, 0) + "'");
                    stmt.executeUpdate("UPDATE laporan SET DateOut = '" + dF.format(dNow) + "', Waktu = '" + rDiff + "', Total='" + total + "' WHERE NoLaporan = '" + NoL + "'");
                    JOptionPane.showMessageDialog(null, "OK, perubahan berhasil");
                    tmplkmr();
                } catch (SQLException ex) {
                    System.out.println(ex);
                }

            }
        } else {
            JOptionPane.showMessageDialog(null, "Mohon maaf kamar ini belum digunakan ");
        }
    }//GEN-LAST:event_checkOutActionPerformed

